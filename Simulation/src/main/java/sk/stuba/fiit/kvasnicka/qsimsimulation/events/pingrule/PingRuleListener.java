package sk.stuba.fiit.kvasnicka.qsimsimulation.events.pingrule;

import java.util.EventListener;

/**
 * notifies listening object when simulation rules are added, removed or changed
 *
 * @author Igor Kvasnicka
 */
public interface PingRuleListener extends EventListener {

    /**
     * A new simulation rule has been added
     * Event contains new simulation rule.
     *
     * @param event
     */
    void pingRuleAdded(PingRuleEvent event);

    /**
     * Simulation rule has been deleted.
     * Event contains removed rule.
     *
     * @param event
     */
    void pingRuleRemoved(PingRuleEvent event);
}
